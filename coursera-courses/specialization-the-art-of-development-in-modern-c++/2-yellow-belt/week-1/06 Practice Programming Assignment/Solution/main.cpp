#include <iostream>

//-------------------------------------------------------------------------------------------------
int main() 
{
	int a = 0;
	std::cin >> a;

	int b = 0;
	std::cin >> b;

	for (size_t i = a; i <= b; i++)
	{
		if (i % 2 == 0) 
		{
			std::cout << i << ' ';
		}
	}
	return 0;
}
